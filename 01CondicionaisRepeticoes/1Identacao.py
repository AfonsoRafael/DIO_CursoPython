# Identação é a forma como organizamos o código em blocos, utilizando espaços ou tabulações.
# Em Python, a identação é obrigatória para definir blocos de código, como em estruturas condicionais, loops e definições de funções.
# Tamém é importante para a legibilidade do código, a estetica é importante.
# Forma correta de identação em Python é geralmente com 4 espaços por nível de indentação.
# Exemplo de identação correta em uma estrutura condicional:
numero = 10
if numero > 5:
    print("O número é maior que 5")
#Fim do bloco if, não há necessidade de chaves ou outros delimitadores.   
else:
    print("O número é 5 ou menor")
# Fim do bloco else.