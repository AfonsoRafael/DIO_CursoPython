# Interfaces em Python são implementadas usando classes abstratas do módulo `abc` (Abstract Base Classes).
# Uma interface define um conjunto de métodos que uma classe deve implementar, sem fornecer a implementação desses métodos.
# O que deve ser feito e não como deve ser feito.
# Isso promove a separação de responsabilidades e facilita a manutenção e evolução do código.
# Python não possui suporte nativo para interfaces como algumas outras linguagens (Java, C#), mas o módulo `abc` permite criar classes abstratas que podem atuar como interfaces.
