# Programação Orientada a Objetos (POO) em Python
# É um paradigma de programação que utiliza "objetos" para representar dados e funcionalidades.

# Objetos são instâncias de classes, que são como moldes para criar esses objetos.
# A POO ajuda a organizar o código, tornando-o mais modular, reutilizável e fácil de manter.
# Conceitos principais da POO:
# 1. Classe: Define a estrutura e o comportamento dos objetos.
# 2. Objeto: Instância de uma classe que contém dados e métodos.
# 3. Atributos: Variáveis que armazenam dados dentro de um objeto.
# 4. Métodos: Funções definidas dentro de uma classe que operam nos dados do objeto.
# 5. Herança: Permite que uma classe herde atributos e métodos de outra classe.
# 6. Encapsulamento: Protege os dados internos de um objeto, restringindo o acesso direto a eles.
# 7. Polimorfismo: Permite que diferentes classes sejam tratadas de maneira uniforme, mesmo que implementem métodos de forma diferente.




# Paradigma procedural foca em funções e lógica sequencial, enquanto POO organiza o código em torno de objetos.
# Paradigma funcional enfatiza funções puras e evita estado mutável, enquanto POO encapsula dados e comportamentos juntos.
# Paradigma orientado a eventos é baseado em eventos e respostas a esses eventos, enquanto POO pode ser usado para modelar sistemas baseados em eventos.